function fill(s: string, v: AgreementTemplateVars) {
  return s
    .replace(/\{\{ownerName\}\}/g, v.ownerName)
    .replace(/\{\{shopName\}\}/g, v.shopName)
    .replace(/\{\{phone\}\}/g, v.phone)
    .replace(/\{\{address\}\}/g, v.address)
    .replace(/\{\{dateFormatted\}\}/g, v.dateFormatted);
}

